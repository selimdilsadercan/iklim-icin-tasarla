def run(
    csv_path: str,
    api_key: str = "",
    output_dir: str = DEFAULT_OUTPUT_DIR,
    print_prompt: bool = False,
) -> dict:

    resolved_key = api_key or os.environ.get("ANTHROPIC_API_KEY", "")
    if not resolved_key:
        raise ValueError(
            "Anthropic API anahtarı bulunamadı.\n"
            "  run() çağrısına api_key='sk-ant-...' geçin\n"
            "  veya ANTHROPIC_API_KEY ortam değişkenini ayarlayın."
        )

    print(f"⏳ İşleniyor: {csv_path}")

    if print_prompt:
        df    = load_labeled_csv(csv_path)
        sname = df[df["Sender"] == "student"]["Student"].iloc[0]
        stats = compute_statistics(df)
        prompt = build_prompt(df, sname, stats)
        print("\n" + "=" * 60)
        print("PROMPT ÖNİZLEME")
        print("=" * 60)
        print(prompt)
        print("=" * 60 + "\n")

    report = process_student(csv_path, resolved_key)
    output_path = save_report(report, output_dir)

    print(f"✅ Rapor kaydedildi: {output_path}\n")
    print(json.dumps(report, ensure_ascii=False, indent=2))
    return report


