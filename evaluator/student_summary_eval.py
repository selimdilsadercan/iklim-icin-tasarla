# 2. İSTATİSTİKSEL ANALİZ
# ─────────────────────────────────────────────────────────────────────────────

def compute_statistics(df: pd.DataFrame) -> dict:
    sdf = df[df["Sender"] == "student"].copy()

    if sdf.empty:
        return {}

    sdf["dialog_num"] = sdf["dialog_label"].map(DIALOG_TO_NUM)

    n = len(sdf)

    # Ortalamalar
    avg_content = sdf["content_label"].mean()
    avg_dialog_num = sdf["dialog_num"].mean()
    avg_dialog_cat = NUM_TO_DIALOG[int(round(avg_dialog_num))]

    # Standart sapma → istikrar
    std_content = sdf["content_label"].std(ddof=1) if n > 1 else 0.0
    stability = "Dengeli" if std_content < 0.7 else "Değişken/İstikrarsız"

    # Gelişim trendi (ilk vs son mesajlar)
    if n >= 4:
        first_score = sdf.iloc[:2]["content_label"].mean()
        last_score  = sdf.iloc[-2:]["content_label"].mean()
    elif n >= 2:
        first_score = float(sdf.iloc[0]["content_label"])
        last_score  = float(sdf.iloc[-1]["content_label"])
    else:
        first_score = last_score = float(sdf.iloc[0]["content_label"])

    if last_score > first_score + 0.5:
        trend = "Yükselen (Öğrenme Gerçekleşiyor)"
    elif last_score < first_score - 0.5:
        trend = "Düşen (Odak Kaybı veya Zorlanma)"
    else:
        trend = "Stabil (Seviye Korunuyor)"

    # İçerik–diyalog uyumu (korelasyon)
    if n > 2:
        corr = sdf["content_label"].corr(sdf["dialog_num"])
        if pd.notna(corr):
            alignment = "Yüksek (Biliyor ve İfade Edebiliyor)" if corr > 0.6 else \
                        "Düşük (Bilgi-İfade Uyumsuzluğu)"
        else:
            alignment = "Hesaplanamadı"
    else:
        alignment = "Yetersiz Veri"

    # Dağılımlar
    content_dist = {str(k): int(v)
                    for k, v in sorted(sdf["content_label"].value_counts().items())}
    dialog_dist  = {str(k): int(v)
                    for k, v in sorted(sdf["dialog_label"].value_counts().items())}

    # Baskın diyalog seviyesi
    dominant_dialog = sdf["dialog_label"].mode().iloc[0] if not sdf["dialog_label"].mode().empty else "?"

    # Genel puan (1-10) — deterministik Python formülü
    # İçerik ağırlığı %60, diyalog %40; her ikisi de 0-3 skalasından normalize edilir.
    content_norm = avg_content / 3.0
    dialog_norm  = avg_dialog_num / 3.0
    overall_score = round((content_norm * 0.6 + dialog_norm * 0.4) * 10, 1)
    # 1-10 sınırlarına sabitle (çok düşük verilerde 0 çıkmasını önler)
    overall_score = max(1.0, min(10.0, overall_score))

    return {
        "overall_score": overall_score,
        "avg_content_score": round(avg_content, 2),
        "avg_dialog_level_numeric": round(avg_dialog_num, 2),
        "avg_dialog_level_category": avg_dialog_cat,
        "dominant_dialog_level": dominant_dialog,
        "content_std": round(std_content, 2),
        "stability": stability,
        "trend": trend,
        "content_dialog_alignment": alignment,
        "content_score_distribution": content_dist,
        "dialog_level_distribution": dialog_dist,
    }


