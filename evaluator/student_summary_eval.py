# 3. LLM PROMPT
# ─────────────────────────────────────────────────────────────────────────────

def build_prompt(df: pd.DataFrame, student_name: str, stats: dict) -> str:
    sdf = df[df["Sender"] == "student"].reset_index(drop=True)

    # ── Mesaj bloğu ──────────────────────────────────────────────────────────
    msg_lines = []
    for i, row in sdf.iterrows():
        msg   = str(row["Message"])[:MAX_MSG_LEN]
        c_lbl = int(row["content_label"])
        d_lbl = str(row["dialog_label"])

        # İçerik gerekçesi: yalnızca ilk cümle
        c_reason = ""
        if pd.notna(row.get("content_reasoning", "")):
            c_reason = str(row["content_reasoning"]).split(".")[0][:MAX_REASON_LEN]

        # Diyalog gerekçesi: yalnızca ilk cümle
        d_reason = ""
        if pd.notna(row.get("dialog_reasoning", "")):
            d_reason = str(row["dialog_reasoning"]).split(".")[0][:MAX_REASON_LEN]

        msg_lines.append(
            f"{i+1}. [C:{c_lbl} D:{d_lbl}] \"{msg}\"\n"
            f"   ↳ {c_reason} | {d_reason}"
        )

    messages_block = "\n\n".join(msg_lines)

    # ── İstatistik özeti ─────────────────────────────────────────────────────
    stats_block = (
        f"Ort. içerik: {stats.get('avg_content_score','?')}/3 | "
        f"Ort. diyalog: {stats.get('avg_dialog_level_category','?')} "
        f"({stats.get('avg_dialog_level_numeric','?')}/3) | "
        f"Baskın diyalog: {stats.get('dominant_dialog_level','?')} | "
        f"Trend: {stats.get('trend','?')} | "
        f"İstikrar: {stats.get('stability','?')} | "
        f"Uyum: {stats.get('content_dialog_alignment','?')}\n"
        f"İçerik dağılımı (0→3): {stats.get('content_score_distribution',{})} | "
        f"Diyalog dağılımı (A→D): {stats.get('dialog_level_distribution',{})}"
    )

    prompt = f"""Sen bir eğitim uzmanısın. Bir ortaokul öğrencisinin iklim/çevre chatbotuyla yaptığı konuşmanın etiketli verisi aşağıda yer alıyor.

SKORLAMA KILAVUZU:
• İçerik (C) 0-3: 0=konu dışı | 1=temel kavram | 2=ileri kavram | 3=çoklu kavram+bağlantı
• Diyalog (D) A-D: A=minimal | B=basit soru | C=diyalogu ilerletici | D=akıl yürütme var

ÖĞRENCİ: {student_name}
İSTATİSTİKLER: {stats_block}

ÖĞRENCİ MESAJLARI:
{messages_block}

Yukarıdaki etiketli veriyi ve istatistikleri kullanarak bu öğrenci için bir değerlendirme raporu oluştur. Yalnızca öğrencinin mesajlarını ve aldığı puanları dikkate al.
Not: Genel puan (overall_score) Python tarafından önceden hesaplanmıştır; senin yalnızca aşağıdaki alanları doldurman bekleniyor.

CEVAP FORMATI (sadece geçerli JSON — başka hiçbir şey yazma):
{{
  "engagement_level": "<Düşük | Orta | Yüksek>",
  "conversation_themes": ["<tema1>", "<tema2>", "<tema3>"],
  "most_interested_topic": "<öğrencinin en çok ilgi duyduğu tek konu>",
  "summary_paragraph": "<öğrencinin konuşmalarını, kavrama düzeyini ve katılım biçimini özetleyen 2-3 cümlelik Türkçe paragraf>"
}}"""

    return prompt


# ─────────────────────────────────────────────────────────────────────────────
# 4. LLM API ÇAĞRISI
# ─────────────────────────────────────────────────────────────────────────────

def call_llm(prompt: str, api_key: str) -> dict:
    """Anthropic Claude API'sini çağırır ve JSON sonucu döndürür."""
    os.environ["ANTHROPIC_API_KEY"] = api_key
    client = anthropic.Anthropic()

    response = client.messages.create(
        model=MODEL,
        max_tokens=512,
        messages=[{"role": "user", "content": prompt}],
    )

    raw = response.content[0].text.strip()

    code_block = re.search(r"```(?:json)?\s*([\s\S]*?)\s*```", raw)
    if code_block:
        raw = code_block.group(1)

    return json.loads(raw)


