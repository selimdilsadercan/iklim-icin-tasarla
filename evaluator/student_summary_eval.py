def process_student(csv_path: str, api_key: str) -> dict:
    """
    Tek bir öğrencinin etiketli CSV'sini işler ve tam rapor dict'i döndürür.
    """
    path = Path(csv_path)
    df = load_labeled_csv(str(path))

    # Öğrenci adı
    student_rows = df[df["Sender"] == "student"]
    if student_rows.empty:
        raise ValueError("CSV'de 'student' göndereni bulunamadı.")
    student_name = student_rows["Student"].iloc[0]

    # Sınıf
    class_name = parse_class_from_path(path)

    # Mesaj sayıları
    total_msg_count   = len(df)
    student_msg_count = len(student_rows)

    # İstatistikler
    stats = compute_statistics(df)

    # LLM promptu ve çağrı
    prompt = build_prompt(df, student_name, stats)
    llm_result = call_llm(prompt, api_key)

    # overall_score Python tarafından hesaplanır; LLM sonucuna eklenir
    llm_result["overall_score"] = stats["overall_score"]

    # Çıktı yapısı — başa meta bilgiler, sona LLM ve Python sonuçları
    report = {
        "student_name":          student_name,
        "class":                 class_name,
        "student_message_count": student_msg_count,
        "total_message_count":   total_msg_count,
        "generated_at":          datetime.now().isoformat(),
        "llm_evaluation":        llm_result,
        "python_computed_stats": stats,
    }

    return report


def save_report(report: dict, output_dir: str = DEFAULT_OUTPUT_DIR) -> str:
    """Raporu JSON dosyasına kaydeder ve dosya yolunu döndürür."""
    os.makedirs(output_dir, exist_ok=True)
    safe_name = re.sub(r"[^\w\s-]", "", report["student_name"]).replace(" ", "_")
    filename  = f"{safe_name}_rapor_{datetime.now().strftime('%Y%m%d_%H%M%S')}.json"
    filepath  = os.path.join(output_dir, filename)

    with open(filepath, "w", encoding="utf-8") as f:
        json.dump(report, f, ensure_ascii=False, indent=2)

    return filepath



def _is_colab() -> bool:
    """Google Colab ortamında çalışıp çalışmadığını tespit eder."""
