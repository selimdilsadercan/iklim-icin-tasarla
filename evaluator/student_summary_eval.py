def parse_class_from_path(csv_path: Path) -> str:
    """
    Klasör adından sınıf bilgisini çıkarır.
    Örn: 'sinif_j.5d_ogrenciler_2025-10-06'  →  'J-5D'
    """
    folder = csv_path.parent.name
    match = re.search(r"sinif_([a-zA-Z]+)\.(\w+)_ogrenciler", folder)
    if match:
        school = match.group(1).upper()
        grade_section = match.group(2).upper()
        return f"{school}-{grade_section}"
    return "Bilinmiyor"


def load_labeled_csv(csv_path: str) -> pd.DataFrame:
    """CSV dosyasını yükler ve temel sütun doğrulaması yapar."""
    df = pd.read_csv(csv_path, encoding="utf-8")

    required = {"Sender", "Message", "Student"}
    missing = required - set(df.columns)
    if missing:
        raise ValueError(f"CSV'de eksik sütunlar: {missing}")

    return df


